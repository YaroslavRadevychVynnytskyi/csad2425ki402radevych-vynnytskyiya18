package application.game;

/**
 * Enum representing the different game modes.
 * Defines the possible modes of the game that players can choose from.
 */
public enum GameMode {
    /**
     * Player vs Player mode where two human players compete against each other.
     */
    MAN_VS_MAN,

    /**
     * Player vs AI mode where a human player competes against an artificial intelligence.
     */
    MAN_VS_AI,

    /**
     * AI vs AI mode where two artificial intelligences compete against each other.
     */
    AI_VS_AI
}
